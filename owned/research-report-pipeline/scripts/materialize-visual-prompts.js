#!/usr/bin/env node

const fs = require("fs");
const path = require("path");
const {
  ensureDir,
  parseArgs,
  readText,
  resolveAbsolute,
  writeText,
} = require("./utils");

const GENERATED_MARKER = "Generated by research-report-pipeline/scripts/materialize-visual-prompts.js";

function usage() {
  console.error("Usage: node materialize-visual-prompts.js --workspace <dir>");
  process.exit(1);
}

function extractVisuals(text) {
  const lines = text.split(/\r?\n/);
  const visuals = [];
  let current = null;

  for (const line of lines) {
    const idMatch = line.match(/^- id:\s*(.+)$/);
    if (idMatch) {
      if (current) {
        visuals.push(current);
      }
      current = { id: idMatch[1].trim(), section: "TODO", visualType: "TODO", purpose: "TODO" };
      continue;
    }
    if (!current) {
      continue;
    }
    const sectionMatch = line.match(/^\s*-\s*section:\s*(.+)$/);
    if (sectionMatch) {
      current.section = sectionMatch[1].trim();
      continue;
    }
    const typeMatch = line.match(/^\s*-\s*visual_type:\s*(.+)$/);
    if (typeMatch) {
      current.visualType = typeMatch[1].trim();
      continue;
    }
    const purposeMatch = line.match(/^\s*-\s*purpose:\s*(.+)$/);
    if (purposeMatch) {
      current.purpose = purposeMatch[1].trim();
      continue;
    }
  }

  if (current) {
    visuals.push(current);
  }
  return visuals;
}

function renderPrompt(visual) {
  return [
    `# ${visual.id}`,
    "",
    `<!-- ${GENERATED_MARKER} -->`,
    `- section: ${visual.section}`,
    `- visual_type: ${visual.visualType}`,
    `- purpose: ${visual.purpose}`,
    "",
    "## Structure Source",
    "",
    "- Reference `notes/diagram-structures.md` before drafting or editing this prompt.",
    "",
    "## Prompt",
    "",
    "TODO",
    "",
  ].join("\n");
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const workspace = resolveAbsolute(args.workspace);
  if (!workspace) {
    usage();
  }

  const inventoryPath = path.join(workspace, "notes/visual-inventory.md");
  const promptsDir = path.join(workspace, "prompts");
  if (!fs.existsSync(inventoryPath)) {
    throw new Error("notes/visual-inventory.md is missing");
  }

  ensureDir(promptsDir);
  const visuals = extractVisuals(readText(inventoryPath));
  for (const visual of visuals) {
    const promptPath = path.join(promptsDir, `${visual.id}.md`);
    if (!fs.existsSync(promptPath)) {
      writeText(promptPath, renderPrompt(visual));
    }
  }

  console.log(promptsDir);
}

main();
