function main() {
  const args = parseArgs(process.argv.slice(2));
  const workspace = resolveAbsolute(args.workspace);
  const bundlePath =
    resolveAbsolute(args.bundle) ||
    (workspace ? path.join(workspace, "notes", "selection-bundle.md") : null);
  const force = Boolean(args.force);

  if (!workspace || !bundlePath) {
    usage();
  }

  const bundle = parseSelectionBundle(readText(bundlePath));
  const profile =
    bundle.visual_profile ||
    bundle.selected_theme ||
    "serious-engineering";
  const styleOverride =
    bundle.visual_style_override &&
    bundle.visual_style_override !== "null" &&
    bundle.visual_style_override !== "false"
      ? bundle.visual_style_override
      : null;

  const configs = configsForProfile(profile);
  const results = [];
  for (const [relativePath, content] of Object.entries(configs)) {
    const targetPath = path.join(workspace, ".baoyu-skills", relativePath);
    const result = maybeWriteText(targetPath, content, {
      force,
      marker: GENERATED_MARKER,
    });
    results.push(`${result.action}: ${targetPath}`);
  }

  console.log(
    [
      `Workspace: ${workspace}`,
      `Profile: ${profile}`,
      styleOverride
        ? `ExplicitStyleOverride: ${styleOverride} (base profile config created; prompt/style patching still required via baoyu-style-bridge)`
        : "ExplicitStyleOverride: none",
      "Results:",
      ...results.map((item) => `- ${item}`),
    ].join("\n"),
  );
}

main();
